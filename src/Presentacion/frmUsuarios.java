 private void Asignar_Valores()
 {
     Usuario.setCedulaUsuario(Integer.parseInt(txtcedulausuario.getText()));
     Usuario.setNombre(txtnombre.getText());
     Usuario.setContraseña(txtcontraseña.getText());
 }
 
