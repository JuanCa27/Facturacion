    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        int resultado;
        Asignar_Valores();
        resultado=Usuario.Nuevo();
        if (resultado==0){
            JOptionPane.showMessageDialog(null, "Se insertaron los datos correctamente");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Ha ocurrido el error numero: " + resultado);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int resultado;
        Asignar_Valores();
        resultado=Usuario.Eliminar();
        if (resultado==0){
            Limpiar_txt();
            JOptionPane.showMessageDialog(null, "Se eliminaron los datos correctamente");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Ha ocurrido el error numero: " + resultado);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
       String pvar=JOptionPane.showInputDialog("Digite la Cedula.");
        Registro=Usuario.Obtener_Datos(pvar);
         try{
    if (Usuario.Obtener_Datos(pvar)!=null)
    {
     Registro.next();
     txtcedulausuario.setText(Registro.getString("cedulaUsuario"));
     txtnombre.setText(Registro.getString("nombre"));
     txtcontraseña.setText(String.valueOf(Registro.getInt("contraseña")));
        }
        }
catch(SQLException e){
    Limpiar_txt(); 
    JOptionPane.showMessageDialog(null, "Cedula no encontrada");
        } 
    }//GEN-LAST:event_btnBuscarActionPerformed

