    private void btnGenerarcompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarcompraActionPerformed
        // TODO add your handling code here:
        String Sql;
        int f;
        int id_Factura=0;
        int resultado, resultado_final;
        //insertar los datos del encabezado, enviandolos a la clase encabezado factura y sus respectivas variables 
Enca.setIdFactura(id_Factura);
Enca.setFecha(fecha_hoy);
Enca.setIdCliente(Integer.parseInt(txtIDcliente.getText()));
Enca.setTotalFactura(Integer.parseInt(txtTotalFactura.getText()));
//la funcion nuevo llama a enviar sentencia y esta me retorna un cero.
        resultado_final=Enca.Nuevo();
       
         //obtener el id que se genero de la boleta
         
if (resultado_final==0){ 
    try{
        //Registro va a valer lo que me retrorne el metodo de resulset Obtener_Id_factura
    Registro=Enca.Obtener_Id_Factura();
    // si Registro es diferente de null
    if (Registro!=null){
    Registro.next();
    // la variable id_Factura va a ser = al id max retornado 
     id_Factura=Registro.getInt("idFactura");
     txtIdFactura.setText(String.valueOf(Registro.getString("idFactura")));
        }                                       
    }
    catch(SQLException e){
   resultado_final=e.getErrorCode();
    } 
}
         
//insertar los productos osea el detalle
if (resultado_final==0){ 
         //hago un for para recorrer la tabla 
         for(f=0;f<=Tabla.getRowCount()-1;f++){
             // indico que los valores de la tabla van a ser ahora los valores del metodo de enca. detalle factura  
             Enca.DetalleFactura.setIdFactura(id_Factura);
             Enca.DetalleFactura.setCodigoProducto(Tabla.getValueAt(f, 0).toString());
             Enca.DetalleFactura.setPrecio(Integer.parseInt(Tabla.getValueAt(f, 2).toString()));
             Enca.DetalleFactura.setCantidad(Integer.parseInt(Tabla.getValueAt(f, 3).toString()));
             Enca.DetalleFactura.setTotalLinea(Integer.parseInt(Tabla.getValueAt(f, 4).toString()));
             
             resultado=Enca.DetalleFactura.Nuevo();
     if (resultado!=0){resultado_final=resultado;}
}
}      
      if (resultado_final==0){
            JOptionPane.showMessageDialog(null, "Se insertaron los datos correctamente");
       } 
       else
       {
            JOptionPane.showMessageDialog(null, "Ha ocurrido el error numero: " + resultado_final);
       }
     
    }//GEN-LAST:event_btnGenerarcompraActionPerformed

