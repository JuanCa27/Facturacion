    public int Total_Factura(){
        //cuento las lineas de la tabla
        int contar=jtfactura.getRowCount();
        int total=0;
        //hago un contador 
        for(int i=0; i<contar;i++){
            //sumo el total registrado y la columna 4 de la tabla
            total=total+Integer.parseInt(jtfactura.getValueAt(i,4).toString());
        }return total;
    }
    
